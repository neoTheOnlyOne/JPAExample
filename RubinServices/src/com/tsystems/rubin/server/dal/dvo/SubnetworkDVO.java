package com.tsystems.rubin.server.dal.dvo;

import java.io.Serializable;

/**
 * The Class SubnetworkDVO.
 */
public class SubnetworkDVO implements Serializable {
	
	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;
	
	/** The id. */
	private Long id;
	
	/** The name. */
	private String name;
	
	/** The label. */
	private String label;

	/**
	 * Instantiates a new subnetwork dvo.
	 * 
	 * @param id the id
	 * @param name the name
	 * @param label the label
	 */
	public SubnetworkDVO(Long id, String name, String label) {
		this.id = id;
		this.name = name;
		this.label = label;
	}

	/**
	 * Gets the id.
	 * 
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * Sets the id.
	 * 
	 * @param id the new id
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * Gets the name.
	 * 
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 * 
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * Gets the label.
	 * 
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * Sets the label.
	 * 
	 * @param label the new label
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "SubnetworkDVO [id=" + id + ", label=" + label + ", name=" + name + "]";
	}
}
