package com.tsystems.rubin.server.dal.dvo;


public class CarrierDVO {

	private Long id;

	private String name;

	private String ituCarrierAbbreviation;

	private String customerId;

	private String code;
	
	private Long countryID;
	
	private String countryId;
	
	public CarrierDVO(Long id, String name, String ituCarrierAbbreviation, String customerId, String code, Long countryID, String countryId, String nationalDesignation) {
		this.id = id;
		this.name = name;
		this.ituCarrierAbbreviation = ituCarrierAbbreviation;
		this.customerId = customerId;
		this.code = code;
		this.countryID = countryID;		
		this.countryId = countryId;
		this.nationalDesignation = nationalDesignation;
	}
	
	public CarrierDVO() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getItuCarrierAbbreviation() {
		return ituCarrierAbbreviation;
	}

	public void setItuCarrierAbbreviation(String ituCarrierAbbreviation) {
		this.ituCarrierAbbreviation = ituCarrierAbbreviation;
	}

	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public Long getCountryID() {
		return countryID;
	}

	public void setCountryID(Long countryID) {
		this.countryID = countryID;
	}

	public String getCountryId() {
		return countryId;
	}

	public void setCountryId(String countryId) {
		this.countryId = countryId;
	}

	public String getNationalDesignation() {
		return nationalDesignation;
	}

	public void setNationalDesignation(String nationalDesignation) {
		this.nationalDesignation = nationalDesignation;
	}

	private String nationalDesignation;

}
