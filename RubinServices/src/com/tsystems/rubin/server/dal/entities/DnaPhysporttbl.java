package com.tsystems.rubin.server.dal.entities;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="DNA_PHYSPORTTBL")
public class DnaPhysporttbl implements Serializable {
	@Id
	@Column(name="OID_PHYSPORTTBL")
	private Long id;

	private String portaddress;

	private String portnumber;

	private String slotaddress;

	private String tid;

	private static final long serialVersionUID = 1L;

	public DnaPhysporttbl() {
		super();
	}

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the portaddress
	 */
	public String getPortaddress() {
		return portaddress;
	}

	/**
	 * @param portaddress the portaddress to set
	 */
	public void setPortaddress(String portaddress) {
		this.portaddress = portaddress;
	}

	/**
	 * @return the portnumber
	 */
	public String getPortnumber() {
		return portnumber;
	}

	/**
	 * @param portnumber the portnumber to set
	 */
	public void setPortnumber(String portnumber) {
		this.portnumber = portnumber;
	}

	/**
	 * @return the slotaddress
	 */
	public String getSlotaddress() {
		return slotaddress;
	}

	/**
	 * @param slotaddress the slotaddress to set
	 */
	public void setSlotaddress(String slotaddress) {
		this.slotaddress = slotaddress;
	}

	/**
	 * @return the tid
	 */
	public String getTid() {
		return tid;
	}

	/**
	 * @param tid the tid to set
	 */
	public void setTid(String tid) {
		this.tid = tid;
	}


}
