package com.tsystems.rubin.server.commonbusinessservices;

import com.tsystems.rubin.server.commonbusinessservices.impl.CircuitCommonServiceImpl;
import com.tsystems.rubin.server.dal.DAOFactory;


/**
 * A factory for creating CircuitCommonService objects.
 */
public class CircuitCommonServiceFactory {
	
	/**
	 * Gets the CircuitCommonService service.
	 * 
	 * @param daoFactory the dao factory
	 * 
	 * @return the CircuitCommonService.
	 */
	public static final CircuitCommonService getCircuitCommonService(DAOFactory daoFactory) {
        return new CircuitCommonServiceImpl(daoFactory);
    }
}
